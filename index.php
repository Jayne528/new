						<div class="answer answer_title">
							避免放置戶外、陽光直曬、或潮濕處，以防天然實木材質因為乾濕度變化過劇，產生形變、褪色。<br>
							放置熱食時須使用隔熱墊、桌巾或玻璃，以免熱氣或油漬損傷木質表面。<br>
							平時可用柔軟的靜電除塵布輕輕去除灰塵。<br>
							若積塵較重，請用微濕軟布輕拭表面灰塵，再以軟質乾布輕輕擦乾。<br>
							如有污漬，請用中性清潔劑或是小蘇打水去除，再以軟質乾布拭乾清潔。<br>
							用乾布沾少許木質保養劑（檸檬蠟）輕拭表面，約半年保養一次。<br>
							避免使用具揮發性的清潔劑以免傷及表面處理之塗料。<br>
							避免使用含矽靈(Silicon)之清潔保養液，以免阻塞實木毛細孔影響觸感。
